package com.example.hpraj.blood_donation.Holder;

public class UserDetailsHolder {

    String fullname, nic, dob, address, contact_no, status, blood_type, mobile, city, nearest_city, type, notifiction_allowed;

    public UserDetailsHolder() {
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getNic() {
        return nic;
    }

    public void setNic(String nic) {
        this.nic = nic;
    }

    public String getDob() {
        return dob;
    }

    public void setDob(String dob) {
        this.dob = dob;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getContact_no() {
        return contact_no;
    }

    public void setContact_no(String contact_no) {
        this.contact_no = contact_no;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getBlood_type() {
        return blood_type;
    }

    public void setBlood_type(String blood_type) {
        this.blood_type = blood_type;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getNearest_city() {
        return nearest_city;
    }

    public void setNearest_city(String nearest_city) {
        this.nearest_city = nearest_city;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getNotifiction_allowed() {
        return notifiction_allowed;
    }

    public void setNotifiction_allowed(String notifiction_allowed) {
        this.notifiction_allowed = notifiction_allowed;
    }
}
